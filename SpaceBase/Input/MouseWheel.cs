namespace Space.Input
{
    /// <summary>
    /// Possible mouse wheel event types.
    /// </summary>
    public enum MouseWheel
    {
        /// <summary>
        /// Invalid value.
        /// </summary>
        None,

        /// <summary>
        /// Mouse wheel scrolled up.
        /// </summary>
        Up,

        /// <summary>
        /// Mouse wheel scrolled down.
        /// </summary>
        Down
    }
}
